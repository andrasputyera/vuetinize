<template>
    <div>
        <v-list-item
          @click="$store.commit('doneTask', task.id)" 
          :class="{ 'teal lighten-5' : task.done }"
        >
          <template v-slot:default>
            <v-list-item-action>
              <!-- Input value determines whether task was done (checks the checkbox)-->
              <v-checkbox
                :input-value="task.done"
                color="primary"
              ></v-checkbox>
            </v-list-item-action>

            <v-list-item-content>
              <v-list-item-title
                :class="{ 'text-decoration-line-through' : task.done }"
              >
                {{ task.title }}
              </v-list-item-title>
            </v-list-item-content>

            <v-list-item-action>
              <!-- The stop method is used to prevent triggering parent element -->
              <v-btn 
              @click.stop="dialogs.delete = true"
              icon
              >
                <v-icon color="teal lighten-3">mdi-delete-empty-outline</v-icon>
              </v-btn>
            </v-list-item-action> 
          </template>
        </v-list-item>
        <v-divider></v-divider>

        <dialog-delete v-if="dialogs.delete" />
    </div>
</template>

<script>
import DialogDelete from './Dialogs/DialogDelete.vue'
export default {
    props: ['task'],
    data() {
      return {
        dialogs: {
          delete: false
        } 
      }
    },
    components: {
      'dialog-delete' : require('@/components/Todo/Dialogs/DialogDelete.vue').default
    }
}
</script>

